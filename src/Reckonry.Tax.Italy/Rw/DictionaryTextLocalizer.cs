    private readonly IReadOnlyDictionary<string, string> languageDictionary;

    private DictionaryTextLocalizer(string language)
    {
        Language = language;
        languageDictionary = language == ReportLanguages.Italian ? Italian : English;
    }

    public string Language { get; }

    public static DictionaryTextLocalizer Create(
        string? language,
        string defaultLanguage = ReportLanguages.English)
    {
        return new DictionaryTextLocalizer(ReportLanguages.NormalizeOrThrow(language, defaultLanguage));
    }

    public string Text(string key)
    {
        if (languageDictionary.TryGetValue(key, out var localized))
        {
            return localized;
        }

        return English.TryGetValue(key, out var fallback) ? fallback : key;
    }

    public string Format(string key, params object[] args)
    {
        return string.Format(CultureInfo.InvariantCulture, Text(key), args);
    }
